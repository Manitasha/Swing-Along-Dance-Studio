<p>We conduct dance workshops and can help develop a memorable and unique corporate team-building experience which helps foster teamwork and trust, while enhancing social skills and promote positivity in your workplace</p>

<p>Dancing as a team building activity can give insight into the dynamics that can exist within the team while having fun as dance has many aspects that require the same skill as teamwork.In order to have a coherent story the participants must be willing to acknowledge the ideas of their teammates and have the ability to embrace their creative problem solving skills and in order to stay on the music one must know how to partner and when to lead and when to follow.</p>

<p> Our team building exercises uses techniques that help people become more self aware, reduce unconscious habits and connect more deeply with their team.These exercises are built to focus on tapping the imagination of our clients.</p>

<p>Team building benefits from having more self awareness, improved connection and creative thinking. These workshops enable a creative environment for colleagues to work together, create a more positive work environment and help develop dancing talents you never knew you had!</p>

<p>We begin with a warm up and group exercises, then teach choreography as a group or in teams, work on formations together and finally give the group a chance to perform. A session can last between for 1.5 to 2 hours and we work closely with each client to customize a Team Building program to meet objectives, team development goals, and desired level of physical activity.
